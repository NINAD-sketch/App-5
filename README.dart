# 🎯 BMI Calculator App

A fun and interactive Flutter app that calculates Body Mass Index (BMI) in real-time.  
Users can select their height and weight using sliders, and the app instantly updates the BMI result along with category indications (Underweight, Normal, Overweight, Obese).

---

## ✨ Features
- 📏 Sliders for height and weight input  
- ⚡ Real-time BMI calculation using `setState()`  
- 🎨 Visually appealing UI with colors/emojis for BMI categories  
- 🔄 Optional reset button for quick restart  

